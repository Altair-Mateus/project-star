    private void jButton_DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_DeletarActionPerformed
        
        try
        {
            //  Atualizando estoque do produto
            models.Produto produto = (models.Produto) jComboBox_Produto_.getSelectedItem();
            produto.setEstoque(produto.getEstoque() + baixa.getQuantidade());
            
            //  Deletando
            DAO.remover(baixa);
            
            //  Mensagem de Salvo
            JOptionPane.showMessageDialog(this, "EXCLUÍDO COM SUCESSO");
            
            //  Limpa Campos
            baixa = new models.BaixaProduto();
            atualizaDados();
            
        }catch(Exception s)
            {
                JOptionPane.showMessageDialog(this, "ERRO:" + s);
            }// Fim do try-catch
    }//GEN-LAST:event_jButton_DeletarActionPerformed

    private void jButton_Salvar_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Salvar_ActionPerformed
        
        try
        {
            //  Instanciando objetos
            baixa = new models.BaixaProduto();
            models.Produto produto = (models.Produto) jComboBox_Produto_.getSelectedItem();

            //  Selecionando os campos a serem salvos
            baixa.setIdProduto(produto);
            baixa.setQuantidade(Integer.parseInt(jTextField_Quantidade_.getText()));
            baixa.setIdChamado((models.Chamado) jComboBox_Chamado_.getSelectedItem());
            baixa.setIdCliente((models.Cliente) jComboBox_Cliente.getSelectedItem());

            //  Chamando método para gravar no banco
            DAO.salvar(baixa);
            
            //  Atualizando estoque do produto
            produto.setEstoque(produto.getEstoque() - baixa.getQuantidade());
            DAO.editar(produto);
            
            //  Mensagem de Salvo
            JOptionPane.showMessageDialog(this, "SALVO COM SUCESSO");
            
            //  Limpa Campos
            baixa = new models.BaixaProduto();
            atualizaDados();
            
        } catch(Exception s)
            {
                JOptionPane.showMessageDialog(this, "ERRO:" + s);
            }// Fim do try-catch
        
        
    }//GEN-LAST:event_jButton_Salvar_ActionPerformed

