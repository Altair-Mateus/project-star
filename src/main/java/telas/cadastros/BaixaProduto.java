    private void atualizaComboBox()
    {
        List produto = new LinkedList();
        produto.add("Selecione");
        produto.addAll(DAO.consulta(models.Produto.class, ""));
        jComboBox_Produto_.setModel(new DefaultComboBoxModel<>(produto.toArray()));
        
        List chamado = new LinkedList();
        chamado.add("Selecione");
        chamado.addAll(DAO.consulta(models.Chamado.class, ""));
        jComboBox_Chamado_.setModel(new DefaultComboBoxModel<>(chamado.toArray()));
        
        List cliente = new LinkedList();
        cliente.add("Selecione");
        cliente.addAll(DAO.consulta(models.Cliente.class, ""));
        jComboBox_Cliente.setModel(new DefaultComboBoxModel<>(cliente.toArray()));
    }// Fim do método atualizaComboBox
    
    private void atualizaDados()
    {
        
        jComboBox_Produto_.setSelectedItem(baixa.getIdProduto());
        jTextField_Quantidade_.setText(baixa.getQuantidade() + "");
        jComboBox_Chamado_.setSelectedItem(baixa.getIdChamado());
        jComboBox_Cliente.setSelectedItem(baixa.getIdCliente());
        
    }// Fim do método atualizaDados

