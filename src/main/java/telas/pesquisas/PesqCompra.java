    //  Método que popula a tabela
    private void atualiza()
    {
        
        String[] titulos = new String[]{"ID", "Produto", "Fornecedor", "Data Compra", "Valor"};
        
        lista = DAO.consultaSQL(models.Compra.class, where);
        
        Object[][] dados = new Object[lista.size()][titulos.length];
        
        for(int i = 0; i < lista.size(); i++)
        {
            
            dados[i][0] = lista.get(i).getId();
            dados[i][1] = lista.get(i).getIdProduto();
            dados[i][2] = lista.get(i).getIdFornecedor();
            dados[i][3] = lista.get(i).dataCompra();
            dados[i][4] = lista.get(i).getValor();
            
        }// Fim do bloco de repetição for
        
        DefaultTableModel dtm = new DefaultTableModel(dados, titulos);
        
        jTable_Compra_.setModel(dtm);     
        
        
    }// Fim do método atualiza
    
    //  Preenchimento dos jComboBox
    private void atualizaComboBox()
    {
        List fornecedor = new LinkedList();
        fornecedor.add("Selecione");
        fornecedor.addAll(DAO.consulta(models.Fornecedor.class, ""));
        jComboBox_Fornecedor_.setModel(new DefaultComboBoxModel<>(fornecedor.toArray()));
        
        List produto = new LinkedList();
        produto.add("Selecione");
        produto.addAll(DAO.consulta(models.Produto.class, ""));
        jComboBox_Produto_.setModel(new DefaultComboBoxModel<>(produto.toArray()));
        
    }// Fim do método atualizaComboBox

