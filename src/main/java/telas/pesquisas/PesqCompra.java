    private void jButton_Pesquisar_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Pesquisar_ActionPerformed

        where = "";
        
        //  Pesquis por id
        if(Utilitarios.isNumeric(jTextField_id_.getText()))
        {
            where += " and o.id='" + jTextField_id_.getText() + "'";
        }// Fim do bloco de condição if
        
        //  Pesquisa por data de abertura
        if(jDateChooser_DataCompra.getDate() != null)
        {
            where += " and o.data_compra='" + new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser_DataCompra.getDate()) + "'";
        }// Fim do bloco de condição if
        
        //  Pesquisa por Produto
        if(jComboBox_Produto_.getSelectedIndex() > 0)
        {
            where += " and o.id_produto='" + ((models.Produto) jComboBox_Produto_.getSelectedItem()).getId()+ "'";
        }// Fim do bloco de condição if
        
        //  Pesquisa por Fornecedor
        if(jComboBox_Fornecedor_.getSelectedIndex() > 0)
        {
            where += " and o.id_fornecedor='" + ((models.Fornecedor) jComboBox_Fornecedor_.getSelectedItem()).getId()+ "'";
        }// Fim do bloco de condição if

        //  Chamando o método que popula a tabela
        atualiza();

    }//GEN-LAST:event_jButton_Pesquisar_ActionPerformed

